#include <stdio.h>

int main() {
    // Define variables
    int A = 10;
    int B = 5;

    // Arithmetic operations
    int sum = A + B;
    int difference = A - B;
    int product = A * B;
    int quotient = A / B;
    int remainder = A % B;

    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", difference);
    printf("Product: %d\n", product);
    printf("Quotient: %d\n", quotient);
    printf("Remainder: %d\n", remainder);

    return 0;
}
